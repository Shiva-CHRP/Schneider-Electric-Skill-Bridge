package schneider.pojo;

public class DepartmentData {
	private String userRoleDepartment;
	private String trainerRoleDepartment;
	private String adminRoleDepartment;

	public String getUserRoleDepartment() {
		return userRoleDepartment;
	}

	public void setUserRoleDepartment(String userRoleDepartment) {
		this.userRoleDepartment = userRoleDepartment;
	}

	public String getTrainerRoleDepartment() {
		return trainerRoleDepartment;
	}

	public void setTrainerRoleDepartment(String trainerRoleDepartment) {
		this.trainerRoleDepartment = trainerRoleDepartment;
	}

	public String getAdminRoleDepartment() {
		return adminRoleDepartment;
	}

	public void setAdminRoleDepartment(String adminRoleDepartment) {
		this.adminRoleDepartment = adminRoleDepartment;
	}

}
